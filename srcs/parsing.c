#include "pipex.h"
#define _POSIX_SOURCE

int	parsing(t_pipex p)
{
	int	i;

	i = -1;
	while (p.c.cmds[++i])
	{
		if (!p.c.cmds[i][0])
			return (0);
	}
	return (1);
}
